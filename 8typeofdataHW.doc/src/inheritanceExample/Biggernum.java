package inheritanceExample;

public class Biggernum {
	// 7. You are given two numbers 50 and 60. How to verify which is bigger
	// number?

	public static void main(String[] args) {

		int i = 50;
		int j = 60;

		if (j > i) {
			System.out.println("bigger number" + j);
		} else {
			System.out.println("bigger number" + i);
		}

	}

}
